#include <iostream>

int main() {
    // Вкладений цикл для рядків
    for (int i = 0; i < 10; ++i) {
        // Вкладений цикл для стовпців
        for (int j = 0; j < 10; ++j) {
            // Друкуємо "0" і пробіл для вирівнювання
            std::cout << "0 ";
        }
        // Перехід до нового рядка після завершення кожного рядка
        std::cout << std::endl;
    }

    return 0;
}
